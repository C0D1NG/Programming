#!/usr/bin/python 
# Python3 program to illustrate 
# hex() function 
  
print("The hexadecimal form of 23 is"
                            + hex(23)) 
                              
print("The hexadecimal form of the "
      "ascii value is 'a' is " + hex(ord('a'))) 
        
print("The hexadecimal form of 3.9 is "
                        + float.hex(3.9)) 
 
